package com.myweathe.app.model;

public class Shi {
	int shiId;
	String shiName;
	String shiCode;
	int shengId;
	public void setShiId(int shiId) {
		this.shiId = shiId;
	}
	public int getShiId() {
		return shiId;
	}
	public void setShiName(String shiName) {
		this.shiName = shiName;
	}
	public String getShiName() {
		return shiName;
	}
	public void setShiCode(String shiCode) {
		this.shiCode = shiCode;
	}
	public String getShiCode() {
		return shiCode;
	}
	public void setShengId(int shengId) {
		this.shengId = shengId;
	}
	public int getShengId() {
		return shengId;
	}
}
