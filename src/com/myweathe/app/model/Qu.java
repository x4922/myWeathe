package com.myweathe.app.model;

public class Qu {
	int quId;
	String quName;
	String quCode;
	int shiId;
	public void setQuId(int quId) {
		this.quId = quId;
	}
	public int getQuId() {
		return quId;
	}
	public void setQuName(String quName) {
		this.quName = quName;
	}
	public String getQuName() {
		return quName;
	}
	public void setQuCode(String quCode) {
		this.quCode = quCode;
	}
	public String getQuCode() {
		return quCode;
	}
	public void setShiId(int shiId) {
		this.shiId = shiId;
	}
	public int getShiId() {
		return shiId;
	}
}
